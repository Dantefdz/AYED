package Ejer5;



public class Datos {
     private int Max;
     private int Min;
     private int Pro;
     
     public void setMax(int max) {
		Max = max;
	}
    public void setMin(int min) {
		Min = min;
	}
    public void setPro(int pro) {
		Pro = pro;
	}
    public int getMax() {
		return Max;
	}
    public int getMin() {
		return Min;
	}
    public int getPro() {
		return Pro;
	}
}
